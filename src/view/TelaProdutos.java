    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        if (jtxtpesquisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Insira um código pra pesquisa");
        } else {
            for (int x = 0; x < tabelaProdutos.getRowCount(); x++) {
                if (jtxtpesquisa.getText().equals(tabelaProdutos.getValueAt(x, 0).toString())) {
                    tabelaProdutos.clearSelection();
                    tabelaProdutos.addRowSelectionInterval(x, x);
                    setarCampos();
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "Pesquisa não encontrou resultados");
        }
        
    }//GEN-LAST:event_jbPesquisarActionPerformed

