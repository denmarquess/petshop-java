    public void setarCampos() {
       
            int setar = tabelaProdutos.getSelectedRow();
            jtxtId.setText(tabelaProdutos.getValueAt(setar, 0).toString());
            jtxtCodigo.setText(tabelaProdutos.getValueAt(setar, 1).toString());
            jtxtDescricao.setText(tabelaProdutos.getValueAt(setar, 2).toString());
            jtxPreco.setText(tabelaProdutos.getValueAt(setar, 3).toString());
            jtxtValidade.setText(tabelaProdutos.getValueAt(setar, 4).toString());
            jtxtMarca.setText(tabelaProdutos.getValueAt(setar, 5).toString());
            jtxtModelo.setText(tabelaProdutos.getValueAt(setar, 6).toString());
            jbCriar.setEnabled(false);
            jtxtCodigo.setEditable(false);
            jbExcluir.setEnabled(true);
            desabilitarEditarCampos();
            
                    
    }
